const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const outputPath = path.join(__dirname, '..', 'src', 'generated', 'buildInfo.ts');

function currentLosAngelesDate() {
  return new Intl.DateTimeFormat('en-CA', {
    timeZone: 'America/Los_Angeles',
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
  }).format(new Date());
}

function resolveGitSha() {
  const vercelSha = process.env.VERCEL_GIT_COMMIT_SHA || process.env.GITHUB_SHA;

  if (vercelSha) {
    return vercelSha.slice(0, 7);
  }

  try {
    return execSync('git rev-parse --short=7 HEAD', {
      cwd: path.join(__dirname, '..'),
      stdio: ['ignore', 'pipe', 'ignore'],
      encoding: 'utf8',
    }).trim();
  } catch {
    return 'local';
  }
}

const buildDate = currentLosAngelesDate();
const gitSha = resolveGitSha();
const contents = `// Generated by scripts/generate-build-info.js. Do not edit by hand.
export const GENERATED_BUILD_DATE = '${buildDate}';
export const GENERATED_GIT_SHA = '${gitSha}';
`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, contents);

console.log(`Generated build info: ${buildDate} ${gitSha}`);
